#ifndef CPP4_3DVIEWER_V2_0_USER_ACTION_H_
#define CPP4_3DVIEWER_V2_0_USER_ACTION_H_

/// \file
/// \brief Перечисление действий пользователя, используемых контроллером/фасадом
///        для управления моделью и параметрами отображения.

/**
 * \enum UserAction_t
 * \brief Команды, поступающие из UI. Часть значений — геометрические
 *        трансформации, часть — действия UI/рендера.
 *
 * Важно: порядок значений используется во внутренней логике (сигналы/слоты,
 * маппинг в фабрике команд). Не вставляйте новые элементы «в середину» —
 * добавляйте только в конец перечисления.
 */
enum class UserAction_t {
  LoadObj = 0,  ///< Загрузка модели из файла OBJ.

  // Трансформации модели:
  MoveModel_X,    ///< Перемещение по оси X.
  MoveModel_Y,    ///< Перемещение по оси Y.
  MoveModel_Z,    ///< Перемещение по оси Z.
  RotateModel_X,  ///< Поворот вокруг оси X (в градусах).
  RotateModel_Y,  ///< Поворот вокруг оси Y (в градусах).
  RotateModel_Z,  ///< Поворот вокруг оси Z (в градусах).
  ScaleModel,     ///< Равномерный масштаб.

  // Параметры рендера/UI:
  SetBackgroundCol,  ///< Установка цвета фона.
  SetVertexCol,      ///< Установка цвета вершин.
  SetEdgeCol,        ///< Установка цвета рёбер.
  SetEdgeWidth,      ///< Установка толщины рёбер.

  // Вывод:
  TakeScreen,  ///< Сохранение скриншота.
  TakeGif,     ///< Сохранение анимации GIF.

  // Виды камеры (ориентации):
  SetViewFront,  ///< Вид спереди  (в сторону -Z).
  SetViewBack,   ///< Вид сзади    (в сторону +Z).
  SetViewLeft,   ///< Вид слева    (в сторону +X).
  SetViewRight,  ///< Вид справа   (в сторону -X).
  SetViewTop,    ///< Вид сверху   (в сторону -Y).
  SetViewBottom  ///< Вид снизу    (в сторону +Y).
};

#endif  // CPP4_3DVIEWER_V2_0_USER_ACTION_H_
